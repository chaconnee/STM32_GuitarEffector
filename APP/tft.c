#define TFT_CS_LOW   HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_RESET)
#define TFT_CS_HIGH  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_SET)
#define TFT_DC_CMD   HAL_GPIO_WritePin(GPIOB, GPIO_PIN_9, GPIO_PIN_RESET)
#define TFT_DC_DATA  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_9, GPIO_PIN_SET)
#define TFT_RST_LOW  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_8, GPIO_PIN_RESET)
#define TFT_RST_HIGH HAL_GPIO_WritePin(GPIOB, GPIO_PIN_8, GPIO_PIN_SET)

#define WIDTH   160
#define HEIGHT  80
#define FONT_W  6
#define FONT_H  10

static void SPI_WriteCmd(uint8_t cmd)
{
    TFT_CS_LOW; TFT_DC_CMD;
    HAL_SPI_Transmit(&hspi1, &cmd, 1, 100);
    TFT_CS_HIGH;
}

static void SPI_WriteCmdEx(uint8_t cmd, uint8_t *data, uint32_t len)
{
    TFT_CS_LOW; TFT_DC_CMD;
    HAL_SPI_Transmit(&hspi1, &cmd, 1, 100);
    if (len) { TFT_DC_DATA; HAL_SPI_Transmit(&hspi1, data, len, 100); }
    TFT_CS_HIGH;
}

static void SPI_WriteData16(uint16_t data)
{
    TFT_CS_LOW; TFT_DC_DATA;
    uint8_t b[2] = { data >> 8, data & 0xFF };
    HAL_SPI_Transmit(&hspi1, b, 2, 100);
    TFT_CS_HIGH;
}

/* Set CASET/RASET window */
static void TFT_SetWindow(uint16_t x, uint16_t y, uint16_t w, uint16_t h)
{
    uint16_t x1 = x + 1, x2 = x + w;
    uint16_t y1 = y + 26, y2 = y + h - 1 + 26;

    SPI_WriteCmd(0x2A);
    TFT_CS_LOW; TFT_DC_DATA;
    { uint16_t v = x1; SPI_WriteData16(v); } { uint16_t v = x2; SPI_WriteData16(v); }
    TFT_CS_HIGH;

    SPI_WriteCmd(0x2B);
    TFT_CS_LOW; TFT_DC_DATA;
    { uint16_t v = y1; SPI_WriteData16(v); } { uint16_t v = y2; SPI_WriteData16(v); }
    TFT_CS_HIGH;
}

