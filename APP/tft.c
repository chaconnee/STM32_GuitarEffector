void TFT_Init(void)
{
    TFT_CS_HIGH; TFT_DC_CMD;
    TFT_RST_LOW;  HAL_Delay(1);
    TFT_RST_HIGH; HAL_Delay(120);

    SPI_WriteCmdEx(0x11, NULL, 0); HAL_Delay(120);
    for (int i = 0; i < sizeof(init_seq)/sizeof(InitCmd); i++)
        SPI_WriteCmdEx(init_seq[i].cmd, (uint8_t *)init_seq[i].param, init_seq[i].len);
    HAL_Delay(120);
    SPI_WriteCmd(0x29);
}

/* ── Test ── */

void TFT_FillScreen(uint16_t color)
{
    TFT_SetWindow(0, 0, WIDTH, HEIGHT);

    SPI_WriteCmd(0x2C);
    TFT_CS_LOW; TFT_DC_DATA;
    uint8_t c[2] = { color >> 8, color & 0xFF };
    uint32_t count = (uint32_t)WIDTH * HEIGHT;
    for (uint32_t i = 0; i < count; i++)
        HAL_SPI_Transmit(&hspi1, c, 2, 100);
    TFT_CS_HIGH;
}

void TFT_Test(void)
{
    TFT_Init();
    TFT_FillScreen(0xFFFF);

    uint16_t colors[] = { 0xF800,0xFF80,0x07E0,0x07FF,0x001F,0xF81F,0xFFE0,0xFFFF };
    const char *text = "chaconne chaconne chaconne chaconne";

    for (int row = 0; row < 8; row++)
        TFT_DrawStr(0, row * 10, text, colors[row]);
}
