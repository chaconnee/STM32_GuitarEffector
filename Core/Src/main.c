/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  SCB->VTOR = FLASH_BASE | 0x4000;

  HAL_RCC_DeInit();
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USB_DEVICE_Init();
  MX_I2S2_Init();
  MX_I2C1_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */

  /* ---- WM8978 + I2S 测试 ---- */

  /* 1. I2C 通信测试: 读 WM8978 寄存器确认 ACK 正常 */
  int32_t ret = WM8978_PORT_Test();
  if (ret != 0)
  {
    /* I2C 失败: LED 快闪 */
    while (1)
    {
      HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
      HAL_Delay(100);
    }
  }

  /* 2. WM8978 上电配置 (模拟块+混音+I2S iface, R2 不含 ADCEN) */
  if (WM8978_PORT_Init() != WM8978_OK)
  {
    /* Codec init 失败: LED 2Hz 闪烁 */
    while (1)
    {
      HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
      HAL_Delay(250);
    }
  }

  /* 3. 启动 I2S DMA (MCLK/BCLK/LRCK 开始输出) */
  AudioPipeline_Init();

  /* 4. 初始化模式控制器 (参数默认值 + LED) */
  ModeCtrl_Init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    AudioPipeline_Tick();
    ModeCtrl_Poll(Pot_Read());
    /* USER CODE END 3 */
  }
}

